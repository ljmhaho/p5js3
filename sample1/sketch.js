  background(240);

  // 화면을 나누는 굵은 선
  stroke(50);
  strokeWeight(3);
  line(0, 200, 600, 200);
  line(300, 0, 300, 400);

  // 사각형 여러 개 겹치기
  noStroke();
  fill(200, 100, 150);
  rect(50, 50, 180, 100);

  fill(100, 150, 220);
  rect(150, 120, 200, 120);

  fill(250, 200, 100);
  rect(320, 250, 180, 100);

  // 원 여러 개
